#  LOOPs
# Repeat a block of code multiple times.
# will print numbers from 0 to 5, here uday is just reference variable
for uday in range(5):
    print(uday)
print("============================================================")

for uday in range(4):
    print("Hello")
print("============================================================")
#it will print numbers from 4 to 9
for uday in range(4, 10):
    print(uday)
print("============================================================")
#to print numbers from 0 to 10 with step of 2, means it will print even numbers from 0-10 except 10
for uday in range(0, 10, 2):
    print(uday)