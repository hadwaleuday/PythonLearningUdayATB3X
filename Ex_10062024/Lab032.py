# Ternary Operator
Uday_marks = 90
Vaibhav_marks = 97

#   x > y -> do something - print("uday")
# y > x -> do something else -> print("vaibhav)
print("Uday is winner" if Uday_marks > Vaibhav_marks else "Vaibhav is winner")


if Uday_marks > Vaibhav_marks:
    print("Uday is the winner")
else:
    print("Vaibhav is the winner")